@Composable
fun PeerReviewTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable() () -> Unit
) {
    val targetColors = if (darkTheme) darkScheme else lightScheme

    val colorScheme = remember {
        mutableStateOf(if (darkTheme) darkScheme else lightScheme)
    }

    LaunchedEffect(targetColors) {
        colorScheme.value = targetColors
    }

    val animatedColors = colorScheme.value.copy(
        primary = animateColorAsState(targetColors.primary).value,
        onPrimary = animateColorAsState(targetColors.onPrimary).value,
        primaryContainer = animateColorAsState(targetColors.primaryContainer).value,
        onPrimaryContainer = animateColorAsState(targetColors.onPrimaryContainer).value,
        secondary = animateColorAsState(targetColors.secondary).value,
        onSecondary = animateColorAsState(targetColors.onSecondary).value,
        secondaryContainer = animateColorAsState(targetColors.secondaryContainer).value,
        onSecondaryContainer = animateColorAsState(targetColors.onSecondaryContainer).value,
        tertiary = animateColorAsState(targetColors.tertiary).value,
        onTertiary = animateColorAsState(targetColors.onTertiary).value,
        tertiaryContainer = animateColorAsState(targetColors.tertiaryContainer).value,
        onTertiaryContainer = animateColorAsState(targetColors.onTertiaryContainer).value,
        error = animateColorAsState(targetColors.error).value,
        onError = animateColorAsState(targetColors.onError).value,
        errorContainer = animateColorAsState(targetColors.errorContainer).value,
        onErrorContainer = animateColorAsState(targetColors.onErrorContainer).value,
        background = animateColorAsState(targetColors.background).value,
        onBackground = animateColorAsState(targetColors.onBackground).value,
        surface = animateColorAsState(targetColors.surface).value,
        onSurface = animateColorAsState(targetColors.onSurface).value,
        surfaceVariant = animateColorAsState(targetColors.surfaceVariant).value,
        surfaceContainer = animateColorAsState(targetColors.surfaceContainer).value,
        onSurfaceVariant = animateColorAsState(targetColors.onSurfaceVariant).value,
        outline = animateColorAsState(targetColors.outline).value,
        inverseOnSurface = animateColorAsState(targetColors.inverseOnSurface).value,
        inverseSurface = animateColorAsState(targetColors.inverseSurface).value,
        inversePrimary = animateColorAsState(targetColors.inversePrimary).value,
        surfaceTint = animateColorAsState(targetColors.surfaceTint).value,
        outlineVariant = animateColorAsState(targetColors.outlineVariant).value,
        scrim = animateColorAsState(targetColors.scrim).value
    )

    MaterialTheme(
        colorScheme = animatedColors,
        typography = PeerReviewTypography(),
        content = content
    )
}